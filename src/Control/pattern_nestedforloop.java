package Control;

import java.util.Scanner;

public class pattern_nestedforloop {

	public static void main(String[] args) 
	{	
		Scanner sc =new Scanner(System.in);
		System.out.println("---PRINT THE STAR PATTERN---");
		
		for(int i =1;i<=3;i++)
		{
			for(int j =1;j<=3;j++)
			{
				System.out.print("* ");
			}
			System.out.println();
		}
		
		

	}

}
